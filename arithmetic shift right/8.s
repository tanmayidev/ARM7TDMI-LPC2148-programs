;ASR Arithmetic Shift Right MOV R0, R2, ASR #1
;Before execution R2 = 0x00000030

		AREA 	ASRProgram, CODE, READONLY
		ENTRY
		
		MOV		R2, #0X00000030			; 0X 0000 0000 0000 0000 0000 0000 0011 0000 IN BIN = 0X00000030
		MOV		R0, R2, ASR #1			; 0X  0000 0000 0000 0000 0000 0000 0001 1000 IN BIN = 0X00000018 IN HEX
		SWI		&11
		END